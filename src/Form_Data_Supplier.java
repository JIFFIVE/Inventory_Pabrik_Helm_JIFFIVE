    private void tampilkanDataSupplier(){
        try{
            Connection conn = DatabaseConnection.connect();
            String sql = "SELECT kode_supplier, nama_supplier, telepon, email, alamat FROM data_supplier";
            PreparedStatement stmt = conn.prepareStatement(sql);
            ResultSet rs = stmt.executeQuery();
            DefaultTableModel model = new DefaultTableModel();
            model.addColumn("Kode Supplier");
            model.addColumn("Nama Supplier");
            model.addColumn("Telepon");
            model.addColumn("Email");
            model.addColumn("Alamat");
            
            while (rs.next()) {
            model.addRow(new Object[]{
                rs.getString("kode_supplier"),
                rs.getString("nama_supplier"),
                rs.getString("telepon"),
                rs.getString("email"),
                rs.getString("alamat"),
                });
            }
            jTable1.setModel(model);
            jTable1.clearSelection();
            jTextField2.setText("");
        } catch (Exception e) {
            JOptionPane.showMessageDialog(this, "Error: " + e.getMessage());
        }
    }
    
    private int TotalData(){
        int total = 0;
        Connection conn = null;
        Statement stmt = null;
        ResultSet rs = null;
        
        try {
            conn = DatabaseConnection.connect();
            stmt = conn.createStatement();
            String sql = "SELECT COUNT(*) AS total FROM data_supplier";
            rs = stmt.executeQuery(sql);
            
            if (rs.next()) {
                total = rs.getInt("total");
            }
        } catch (Exception e) {
            JOptionPane.showMessageDialog(null, "Error: " + e.getMessage());
        } finally {
            try {
                if (rs != null) rs.close();
                if (stmt != null) stmt.close();
                if (conn != null) conn.close();
            } catch (Exception e) {
                e.printStackTrace();
            }
        }
        return total;
    }
    
    private void loadTotalData(){
        int total = TotalData();
        jTextField1.setText(String.valueOf(total));
    }
    

            String sql = "INSERT INTO data_supplier (kode_supplier, nama_supplier, telepon, email, alamat) VALUES (?, ?, ?, ?, ?)";
            PreparedStatement stmt = conn.prepareStatement(sql);
            
            stmt.setString(1, kode_supplier);
            stmt.setString(2, nama_supplier);
            stmt.setString(3, telepon);
            stmt.setString(4, email);
            stmt.setString(5, alamat);
            stmt.executeUpdate();

